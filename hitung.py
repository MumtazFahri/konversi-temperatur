angka1 = float(input("Masukkan angka pertama: "))
angka2 = float(input("Masukkan angka kedua: "))

hasil = angka1 + angka2
print("Hasil penjumlahan: ", hasil)

hasil = angka1 - angka2
print("Hasil pengurangan: ", hasil)

hasil = angka1 * angka2
print("Hasil perkalian: ", hasil)

hasil = angka1 / angka2
print("Hasil pembagian: ", hasil)